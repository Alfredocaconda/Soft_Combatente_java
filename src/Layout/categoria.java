    private void guardar() {
        Mcategoria modal = new Mcategoria();
        String nome = txtnome.getText();
        if (!nome.isEmpty()) {
            //instanciamos a classe control
            Ccategoria control = new Ccategoria();
            modal.setIdCategoria(id);
            modal.setNomeC(nome);
            //recuperar os dados na control
            if (control.salvar(modal) == 1) {
                modal.mensagem("Sucesso");
            } else {
                modal.mensagem("Erro");
            }
        } else {
            modal.mensagem("Prencha os campos");
        }
        txtnome.requestFocus();
        txtnome.setText("");
    }

    private void listar() {
        Ccategoria c = new Ccategoria();
        DefaultTableModel dtm = (DefaultTableModel) tbcategoria.getModel();

        Object[] titulo = {"Codigo", "Nome da Categoria"};
        dtm.setColumnIdentifiers(titulo);
        dtm.setNumRows(0);
        for (Mcategoria item : c.consulta()) {
            Object[] dado = {item.getIdCategoria(), item.getNomeC()};
            dtm.addRow(dado);
        }
    }

    private void apagar() {
        int id = (int) tbcategoria.getValueAt(tbcategoria.getSelectedRow(), 0);
        Mcategoria m = new Mcategoria();
        m.setIdCategoria(id);
        Ccategoria c = new Ccategoria();
        c.eliminar(m);

    }

    private void atualizar() {
        //instanciamos a classe modal
        Mcategoria modal = new Mcategoria();
        String nome = txtnome.getText();
        if (!nome.isEmpty()) {
            //instanciamos a classe control
            Ccategoria control = new Ccategoria();
            modal.setNomeC(nome);
            modal.setIdCategoria(id);
            control.atualizar(modal);
            //recuperar os dados na control
        } else {
            modal.mensagem("Prencha os campos");
        }
        txtnome.requestFocus();
        txtnome.setText("");
    }

    private void pesquisar(String pesquisar) {
        Ccategoria c = new Ccategoria();
        DefaultTableModel dtm = (DefaultTableModel) tbcategoria.getModel();

        Object[] titulo = {"Codigo", "Nome da Categoria"};
        dtm.setColumnIdentifiers(titulo);
        dtm.setNumRows(0);
        for (Mcategoria item : c.pesquisar(pesquisar)) {
            Object[] dado = {item.getIdCategoria(), item.getNomeC()};
            dtm.addRow(dado);
        }

    }
