    private void tbcategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbcategoriaMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            Object[] option = {"Apagar", "Cancelar", "Editar"};// derclaramos um vetor objecto por onde estara escrito o que desejamos no botao JOptionPane
            int valor = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, option[1]);
            //OPTION[1] É PARA FOCAR NO BOTÃO CANCELAR
            if (valor == 0) {
                apagar();
                listar();
            } else {
                if (valor == 2) {
                    id = (int) tbcategoria.getValueAt(tbcategoria.getSelectedRow(), 0);
                    txtnome.setText((String) tbcategoria.getValueAt(tbcategoria.getSelectedRow(), 1));
                    btnguardar.setText("Actualizar");
                }

            }
        }
    }//GEN-LAST:event_tbcategoriaMouseClicked

