    private void salvar() {
        String cidade = txtcidade.getText();
        String provincia = txtprovincia.getText();
        Mendereco model = new Mendereco();
        Cendereco control = new Cendereco();
        if (!cidade.isEmpty() && !provincia.isEmpty()) {
            model.setCidade(cidade);
            model.setProvincia(provincia);
            control.salvar(model);
        } else {
            model.mensagem("Prencha os campos");
        }
        txtcidade.requestFocus();
        txtcidade.setText("");
        txtprovincia.setText("");
    }

    private void listar() {
        Cendereco c = new Cendereco();
        DefaultTableModel dtm = (DefaultTableModel) tbEndereco.getModel();

        Object[] titulo = {"Codigo", "Cidade", "Provinçia"};
        dtm.setColumnIdentifiers(titulo);
        dtm.setNumRows(0);
        for (Mendereco item : c.listar()) {
            Object[] dado = {item.getIdEndereco(), item.getCidade(), item.getProvincia()};
            dtm.addRow(dado);
        }
    }

    private void eliminar() {
        int n = (int) tbEndereco.getValueAt(tbEndereco.getSelectedRow(), 0);
        Mendereco m = new Mendereco();
        m.setIdEndereco(n);
        Cendereco ende = new Cendereco();
        ende.apagar(m);

    }

    private void atualizar() {
        String cidade = txtcidade.getText();
        String provincia = txtprovincia.getText();
        Mendereco model = new Mendereco();
        Cendereco control = new Cendereco();
        if (!cidade.isEmpty() && !provincia.isEmpty()) {
            model.setCidade(cidade);
            model.setIdEndereco(id);
            model.setProvincia(provincia);
            control.atualizar(model);
            txtcidade.requestFocus();
            txtcidade.setText("");
            txtprovincia.setText("");
        } else {
            model.mensagem("Prencha os campos");
        }

    }

    private void pesquisar(String pesquisar) {
        Cendereco c = new Cendereco();
        DefaultTableModel dtm = (DefaultTableModel) tbEndereco.getModel();

        Object[] titulo = {"Codigo", "Cidade", "Provinçia"};
        dtm.setColumnIdentifiers(titulo);
        dtm.setNumRows(0);
        for (Mendereco item : c.pesquisar(pesquisar)) {
            Object[] dado = {item.getIdEndereco(), item.getCidade(), item.getProvincia()};
            dtm.addRow(dado);
        }
    }
