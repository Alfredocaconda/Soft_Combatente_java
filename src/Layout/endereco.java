    private void tbEnderecoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEnderecoMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            ui = null;
            UIManager.put("OptionPane.background", new Color(36, 136, 203));// para por cor do fundo do JOptionPane
            UIManager.put("Panel.background", new Color(36, 136, 203));
            Object[] option = {"Apagar", "Cancelar", "Editar"};// derclaramos um vetor objecto por onde estara escrito o que desejamos no botao JOptionPane
            int valor = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, option[1]);
            //OPTION[1] É PARA FOCAR NO BOTÃO CANCELAR
            if (valor == 0) {
                eliminar();
                listar();
            } else {
                if (valor == 2) {
                    id = (int) tbEndereco.getValueAt(tbEndereco.getSelectedRow(), 0);
                    txtcidade.setText((String) tbEndereco.getValueAt(tbEndereco.getSelectedRow(), 1));
                    txtprovincia.setText((String) tbEndereco.getValueAt(tbEndereco.getSelectedRow(), 2));
                    btnsalvar.setText("Actualizar");
                }

            }
        }
    }//GEN-LAST:event_tbEnderecoMouseClicked

