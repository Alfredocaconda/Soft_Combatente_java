    private String data() {
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyy/MM/dd");
        String dt = s.format(d);
        txtdata.setText(dt);
        return dt;
    }

    private void listar() {
        Cdesmobilizado cd = new Cdesmobilizado();// instanciar a control 
        DefaultTableModel dtm = (DefaultTableModel) tbdesmobilizado.getModel();//instanciamos a classe default
        Object[] dados = {"CODIGO", "NOME", "GENERO", "NASCIMENTO", "Nº BI", "DATA DO CADASTRO", "CATEGORIA",
            "SALÁRIO", "MUNICIPIO", "PROVINCIA"};//declaramos um objecto que vai mostrar os titulos na tabela
        dtm.setColumnIdentifiers(dados);//adicionamos na tabela
        dtm.setNumRows(0);//limpamos na tabela caso tenha um item
        for (MVdesmobilizado md : cd.consultar()) {
            Object[] recebe = {md.getIdMobilizado(), md.getNome(), md.getGenero(),
                md.getDataNascimento(), md.getBi(), md.getDataCadastro(), md.getCategoria(), md.getSalario() + "  KZ ", md.getMunicipio(), md.getProvincia()};
            dtm.addRow(recebe);
        }
    }

    private void apagar() {
        Mdesmobilizado md = new Mdesmobilizado();//instanciamos a modal
        String valor = (String) tbdesmobilizado.getValueAt(tbdesmobilizado.getSelectedRow(), 0);//declaramos uma variavel que recebe a um valor da tabela caso seja seleciona e é convertido
        md.setIdMobilizado(Integer.valueOf(valor));
        Cdesmobilizado cd = new Cdesmobilizado();//instanciamos a control
        cd.delete(md);//chamamos o medoto delete que esta na control desmobilizado
    }

