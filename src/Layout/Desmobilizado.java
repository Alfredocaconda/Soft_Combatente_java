    private void tbdesmobilizadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdesmobilizadoMouseClicked
        // TODO add your handling code here:
        id = (String) tbdesmobilizado.getValueAt(tbdesmobilizado.getSelectedRow(), 0);
        if (evt.getClickCount() == 2) {
            Object[] recebe = {"Apagar", "Cancelar", "Editar", "Imprimir"};//declaramos uma vetor objecto que vai armazenar os itens selecionado
            int n = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, recebe, recebe[1]);
            if (n == 0) {
                apagar();
                listar();
            } else {
                if (n == 2) {
                    txtnome.setText((String) tbdesmobilizado.getValueAt(tbdesmobilizado.getSelectedRow(), 1));
                    txtbi.setText((String) tbdesmobilizado.getValueAt(tbdesmobilizado.getSelectedRow(), 4));
                    txtsalario.setText((String) tbdesmobilizado.getValueAt(tbdesmobilizado.getSelectedRow(), 7));
                    cont = 1;
                    btnsalvar.setText("Actualizar");
                }
                if (n == 3) {
                    Cdesmobilizado f = new Cdesmobilizado();
                    f.imprimir1(id + "");
                }
            }
        }
    }//GEN-LAST:event_tbdesmobilizadoMouseClicked

