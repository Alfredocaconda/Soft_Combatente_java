    private void tbcargoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbcargoMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            Object[] option = {"Apagar", "Cancelar", "Editar"};// derclaramos um vetor objecto por onde estara escrito o que desejamos no botao JOptionPane
            int valor = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, option[1]);
            //OPTION[1] É PARA FOCAR NO BOTÃO CANCELAR
            if (valor == 0) {
                apagar();
                listardados();
            } else {
                if (valor == 2) {

                    id = (int) tbcargo.getValueAt(tbcargo.getSelectedRow(), 0);
                    txtnome.setText((String) tbcargo.getValueAt(tbcargo.getSelectedRow(), 1));
                    btnguardar.setText("Actualizar");
                }

            }
        }

    }//GEN-LAST:event_tbcargoMouseClicked

