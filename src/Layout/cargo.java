    private void atualizar() {
        Mcargo modal = new Mcargo();//instanciamos a classe modal do cargo
        String nome = txtnome.getText();//atribuimos o valor na variavel nome
        if (!nome.isEmpty()) {
            Ccargo control = new Ccargo();//instanciamos a classe control do cargo

            modal.setNomeC(nome);
            modal.setIdCargo(id);
            control.atualizar(modal);// recuperar na control
        } else {
            modal.mensagem("Prencha todos os campos");
        }
        txtnome.requestFocus();
        txtnome.setText("");
    }

    private void guardar() {
        Mcargo modal = new Mcargo();//instanciamos a classe modal do cargo
        String nome = txtnome.getText();//atribuimos o valor na variavel nome
        if (!nome.isEmpty()) {
            Ccargo control = new Ccargo();//instanciamos a classe control do cargo
            modal.setNomeC(nome);
            control.salvar(modal);// recuperar na control
        } else {
            modal.mensagem("Prencha todos os campos");
        }
        txtnome.requestFocus();
        txtnome.setText("");
    }

    private void listardados() {
        Ccargo c = new Ccargo();// instanciamos a classe control do cargo
        DefaultTableModel dtm = (DefaultTableModel) tbcargo.getModel();//instanciamos a classe default Table Model 
        Object[] dados = {"CODIGO", "CARGO"};// declaramos um vector Objecto para mostar na tabela
        dtm.setColumnIdentifiers(dados);// add na coluna
        dtm.setNumRows(0);// removemos as linhas até 0

//condicão aprimorado, for que nos permite apresentar os dados na base de dados na tabela
        for (Mcargo m : c.consulta()) {
            Object[] da = {m.getIdCargo(), m.getNomeC()};// declaramos um vector Objecto para mostar na tabela
            dtm.addRow(da);//adicionamos na linha da tabela
        }

    }

    private void apagar() {
        int n = (int) tbcargo.getValueAt(tbcargo.getSelectedRow(), 0);// declaramos uma variavel inteiro que vai receber a linha da tabela selecionada
        Mcargo m = new Mcargo();//instanciamos a classe modal do cargo
        m.setIdCargo(n);
        Ccargo ende = new Ccargo();//instanciamos a classe control do cargo
        ende.apagar(m);

    }

    private void pesquisar(String pesquisar) {
        Ccargo c = new Ccargo();// instanciamos a classe control do cargo
        DefaultTableModel dtm = (DefaultTableModel) tbcargo.getModel();//instanciamos a classe default Table Model 
        Object[] dados = {"CODIGO", "CARGO"};// declaramos um vector Objecto para mostar na tabela
        dtm.setColumnIdentifiers(dados);// add na coluna
        dtm.setNumRows(0);// removemos as linhas até 0

//condicão aprimorado, for que nos permite apresentar os dados na base de dados na tabela
        for (Mcargo m : c.pesquisar(pesquisar)) {
            Object[] da = {m.getIdCargo(), m.getNomeC()};// declaramos um vector Objecto para mostar na tabela
            dtm.addRow(da);//adicionamos na linha da tabela
        }

    }
