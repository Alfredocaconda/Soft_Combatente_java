    private void pesquisar(String pesquisar) {
        Ctarefa ct = new Ctarefa();
        DefaultTableModel dtm = (DefaultTableModel) tbtarefa.getModel();
       Object[] dados = {"CODIGO", "TAREFA", "DATA DO INICIO", "DATA DO TERMINO", "ESTADO DA TAREFA", "DESCRIÇÃO","DEPARTAMENTO"};
        dtm.setColumnIdentifiers(dados);
        dtm.setNumRows(0);
        for (Modelo.MVTAREFA item : ct.pesquisar(pesquisar)) {
            Object[] dad = {item.getCODIGO(), item.getNomeDaTarefa(), item.getInicio(), item.getTermino(),
                item.getEstado(), item.getDescricao(),item.getDepartamento()};
            dtm.addRow(dad);
        }

    }
