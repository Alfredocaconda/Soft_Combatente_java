    private void departamento() {
        Cdepartamento c = new Cdepartamento();
        for (Mdepartamento m : c.listar()) {
            cbdepartamento.addItem(m);
        }
    }

    private void listartarefa() {
        Ctarefa t = new Ctarefa();
        DefaultTableModel tb = (DefaultTableModel) tbtarefa.getModel();
        Object[] dados = {"CODIGO", "TAREFA", "DATA DO INICIO", "DATA DO TERMINO", "ESTADO DA TAREFA", "DESCRIÇÃO","DEPARTAMENTO"};
        tb.setColumnIdentifiers(dados);
        tb.setNumRows(0);
        for (Modelo.MVTAREFA item : t.listar()) {
            Object[] dad = {item.getCODIGO(), item.getNomeDaTarefa(), item.getInicio(), item.getTermino(),
                item.getEstado(), item.getDescricao(),item.getDepartamento()};
            tb.addRow(dad);
        }

    }

