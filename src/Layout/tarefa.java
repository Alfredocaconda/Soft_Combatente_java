    private void tbtarefaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbtarefaMouseClicked
        // TODO add your handling code here:
        id = (int) tbtarefa.getValueAt(tbtarefa.getSelectedRow(), 0);
        if (evt.getClickCount() == 2) {
            Object[] option = {"Apagar", "Cancelar", "Editar","imprimir"};// derclaramos um vetor objecto por onde estara escrito o que desejamos no botao JOptionPane
            int valor = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, option[1]);
            //OPTION[1] É PARA FOCAR NO BOTÃO CANCELAR
            if (valor == 0) {
                apagar();
                listartarefa();
            } else {
                if (valor == 2) {
                    txttarefa.setText((String) tbtarefa.getValueAt(tbtarefa.getSelectedRow(), 1));
                    txtdatainicio.setText((String) tbtarefa.getValueAt(tbtarefa.getSelectedRow(), 2));
                    txtdatatermino.setText((String) tbtarefa.getValueAt(tbtarefa.getSelectedRow(), 3));
                    txtdescricao.setText((String) tbtarefa.getValueAt(tbtarefa.getSelectedRow(), 5));
                    btnsalvar.setText("Actualizar");
                }
                if (valor == 3) {
                    Ctarefa d = new Ctarefa();
                    d.imprimir(id + "");
                }
            }
        }
    }//GEN-LAST:event_tbtarefaMouseClicked

